function handlePhysicalKeyboard(event) {
    // Игнорируем нажатия, если модальное окно настроек открыто
    if (document.getElementById('settings-dialog').style.display === 'block') {
        return;
    }

    const key = event.key.toUpperCase(); // Получаем нажатую клавишу

    // Проверяем, является ли клавиша буквой (А-Я или A-Z)
    const isLetter = key.length === 1 && key.match(/[А-ЯA-Z]/);
    
    if (isLetter) {
        // Имитируем клик по виртуальной кнопке
        const virtualButton = document.querySelector(`button[data-key="${key}"]`);
        if (virtualButton) {
            virtualButton.click(); // Это вызовет уже существующий handleKeyboardClick
        }
    } else if (key === 'BACKSPACE') {
        // Имитируем нажатие кнопки "Удалить"
        const deleteButton = document.querySelector('button[data-key="DELETE"]');
        deleteButton?.click();
    } else if (key === 'ENTER') {
        // Имитируем нажатие кнопки "Ввод"
        const enterButton = document.querySelector('button[data-key="ENTER"]');
        enterButton?.click();
    }
}

// --- ГЛАВНАЯ ФУНКЦИЯ ОТРИСОВКИ ---
function renderEntireGame() {
    gridElement.innerHTML = ''; // Полная очистка перед перерисовкой

    // 1. Отрисовываем завершенные ходы (раскрашиваем плитки)
    guesses.forEach((guess, rowIndex) => {
        const rowDiv = document.createElement('div');
        rowDiv.classList.add('grid-row');
        
        const colors = calculateTileColors(guess);
        
        guess.split('').forEach((letter, colIndex) => {
            const tile = document.createElement('div');
            tile.classList.add('tile');
            
            if (letter) {
                tile.textContent = letter;
                tile.classList.add(colors[colIndex]); // Добавляем цвет: green/yellow/gray
                
                tile.classList.add('has-text'); // Делаем видимым
            }
            rowDiv.appendChild(tile);
        });
        gridElement.appendChild(rowDiv);
    });

    // 2. Отрисовываем текущий ряд (где идет ввод)
    if (currentRow.length > 0) {
        const rowDiv = document.createElement('div');
        rowDiv.classList.add('grid-row');
        
        currentRow.forEach((letter, colIndex) => {
            const tile = document.createElement('div');
            tile.classList.add('tile');
            
            if (letter) {
                tile.textContent = letter;
                tile.classList.add('has-text'); // Буквы текущего хода всегда видны
            }
            
            // Подсветка активной плитки
            if (colIndex === currentCol) {
                if (!isGameOver) {
                tile.classList.add('active');
                }
                // Принудительно снимаем фокус с кнопок и ставим на активную плитку
                document.activeElement?.blur();
                if (!isGameOver) {
                tile.focus(); 
                }
            }
            
            rowDiv.appendChild(tile);
        });
        gridElement.appendChild(rowDiv);
    }
}

// --- ФУНКЦИЯ ПОДСЧЕТА ЦВЕТОВ (логика Wordle) ---
function calculateTileColors(guess) {
    const result = [];
    const letterCount = {};
    
    // Запас букв из ответа
    answer.split('').forEach(char => {
        letterCount[char] = (letterCount[char] || 0) + 1;
    });

    // Фаза 1: Зеленые (точные совпадения)
    guess.split('').forEach((char, i) => {
        if (char === answer[i]) {
            result.push('green');
            letterCount[char]--;
        } else {
            result.push(null);
        }
    });

    // Фаза 2: Желтые (буква есть, но не на месте)
    guess.split('').forEach((char, i) => {
        if (result[i]) return; // Уже зеленая

        if (letterCount[char] > 0) {
            result[i] = 'yellow';
            letterCount[char]--;
        } else {
            result[i] = 'gray';
        }
    });

    return result;
}

// --- ОБРАБОТЧИК КЛАВИАТУРЫ ---
function handleKeyboardClick(e) {
    if (isGameOver) return;
    const key = e.target.dataset.key;

    if (key === "DELETE") {
        currentRow[currentCol] = '';
        if (currentCol > 0) {
            currentCol--;
        }
        renderEntireGame();

    } else if (key === "ENTER") {
        submitCurrentGuess();

    } else if (key && currentCol < WORD_LENGTH) {
    // Если клавиша — буква и мы не вышли за пределы поля
    currentRow[currentCol] = key; // Ставим букву в текущую ячейку

    // Проверка: если текущая ячейка НЕ последняя, переходим к следующей
    if (currentCol < WORD_LENGTH - 1) {
        currentCol++; // <-- Только здесь меняем индекс
    }
    // Если же мы уже на последней ячейке (4 из 5), мы на ней и остаемся.
    // Отправка будет происходить только по нажатию ENTER.
    
    renderEntireGame(); // Перерисовываем поле
}
}

// --- ЛОГИКА ОТПРАВКИ ХОДА ---
function submitCurrentGuess() {
    const guessWord = currentRow.join('').toUpperCase();
    
    if (!guessWord || guessWord.length !== WORD_LENGTH || !words.includes(guessWord)) {
        // Слово не подходит или не в словаре
        if (guessWord.length !== WORD_LENGTH) {
            messageSpan.textContent = 'Слово должно быть из 5 букв!';
        } else {
        messageSpan.textContent = 'Слова "' + guessWord + '" не существует!';
        }
        return; 
    } messageSpan.textContent = '';
                        // --- НОВАЯ МАГИЯ ---
// Находим контейнер всей сетки
const gridContainer = document.querySelector('#grid');

// Добавляем класс-триггер, который запустит анимацию
gridContainer.classList.add('animate-tiles');

// Удаляем этот класс через 500мс (чтобы анимация могла завершиться)
setTimeout(() => {
    gridContainer.classList.remove('animate-tiles');
}, 500);
    guesses.push(guessWord);
    
    if (guessWord === answer) {
        endGame(true);
    } else if (guesses.length >= MAX_GUESSES) {
        endGame(false);
    } else {
        // Переходим к следующему ряду
        currentRow = new Array(WORD_LENGTH).fill('');
        currentCol = 0;
        renderEntireGame();
    }
}

// --- СТАРТ НОВОЙ ИГРЫ ---
function setupGame() {
    isGameOver = false;
    startButton.style.visibility = 'hidden';
    answer = pickRandomWord(words);
    
    // Сброс состояния для новой игры
    guesses = [];
    currentRow = new Array(WORD_LENGTH).fill('');
    currentCol = 0;
    messageSpan.textContent = '';
        document.addEventListener('keydown', handlePhysicalKeyboard);
    renderEntireGame();
}

// --- КОНЕЦ ИГРЫ ---
function endGame(won) {
    isGameOver = true;
    startButton.style.visibility = 'visible';
    
    if (won) {
        messageSpan.textContent = 'Поздравляю! Вы выиграли!';
    } else {
        messageSpan.textContent = `Игра закончена. Ответ был "${answer}".`;
    }
}

// --- СБРОС СОСТОЯНИЯ ИГРЫ ---
function resetGameState() {
    answer = '';
    guesses = [];
    currentRow = new Array(WORD_LENGTH).fill('');
    currentCol = 0;
}

// --- ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ ---
function pickRandomWord(wordList) {
    return wordList[Math.floor(Math.random() * wordList.length)];
}

